    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().isEmpty() || txtApellidos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete datos");
            return;
        }

        Docente d = new Docente();
        d.setDni(txtDni.getText());
        d.setNombres(txtNombre.getText());
        d.setApellidos(txtApellidos.getText());
        d.setTelefono(txtTelefono.getText());
        d.setCorreo(txtCorreo.getText());
        d.setPagoHora(Double.parseDouble(txtPagoPorHora.getText()));
        d.setEstado(true);

        if (dao.registrar(d)) {
            JOptionPane.showMessageDialog(this, "Docente registrado");
            listar();
            limpiar();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (idDocente == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione docente");
            return;
        }

        Docente d = new Docente();
        d.setIdDocente(idDocente);
        d.setDni(txtDni.getText());
        d.setNombres(txtNombre.getText());
        d.setApellidos(txtApellidos.getText());
        d.setTelefono(txtTelefono.getText());
        d.setCorreo(txtCorreo.getText());
        d.setPagoHora(Double.parseDouble(txtPagoPorHora.getText()));
        d.setEstado(true);

        if (dao.actualizar(d)) {
            JOptionPane.showMessageDialog(this, "Actualizado");
            listar();
            limpiar();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (idDocente == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione docente");
            return;
        }

        if (dao.eliminar(idDocente)) {
            JOptionPane.showMessageDialog(this, "Eliminado");
            listar();
            limpiar();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        limpiar();
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void tblDocenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDocenteMouseClicked
        int fila = tblDocente.getSelectedRow();

        idDocente = Integer.parseInt(modelo.getValueAt(fila, 0).toString());
        txtDni.setText(modelo.getValueAt(fila, 1).toString());
        txtNombre.setText(modelo.getValueAt(fila, 2).toString());
        txtApellidos.setText(modelo.getValueAt(fila, 3).toString());
        txtTelefono.setText(modelo.getValueAt(fila, 4).toString());
        txtCorreo.setText(modelo.getValueAt(fila, 5).toString());
        txtPagoPorHora.setText(modelo.getValueAt(fila, 6).toString());

    }//GEN-LAST:event_tblDocenteMouseClicked

    private void listar() {

        List<Docente> lista = dao.listar();
        modelo = (DefaultTableModel) tblDocente.getModel();
        modelo.setRowCount(0);

        for (Docente d : lista) {
            modelo.addRow(new Object[]{
                d.getIdDocente(),
                d.getDni(),
                d.getNombres(),
                d.getApellidos(),
                d.getTelefono(),
                d.getCorreo(),
                d.getPagoHora()
            });
        }
    }

    private void limpiar() {
        txtDni.setText("");
        txtNombre.setText("");
        txtApellidos.setText("");
        txtTelefono.setText("");
        txtCorreo.setText("");
        txtPagoPorHora.setText("");
        idDocente = 0;
    }
